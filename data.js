// Main function to return real GA4 data
// Replaces the old random simulation engine
function buildDashboardData(startDate, endDate, compareMode, sourceFilter, catFilter, prodFilter, campaignFilter, canalFilter, grupoFilter) {
  const processPeriod = (period) => {
    if (!period) {
      return {
        kpis: { sessions: 0, users: 0, purchases: 0, revenue: 0, engagementRate: 0, bounceRate: 0, avgDuration: 0 },
        funnel: { page_view: 0, view_item: 0, add_to_cart: 0, begin_checkout: 0, add_personal_info: 0, add_shipping_info: 0, add_payment_info: 0, purchase: 0 },
        sparklines: { sessoes: [], users: [], purchases: [], rev: [], engagement: [], duration: [] },
        devices: [], regions: [], cities: [], products: [], categories: [], channels: [], channelGroups: []
      };
    }

    const rawKpis = period.kpis || { sessions: 0, users: 0, purchases: 0, revenue: 0, engagementRate: 0, bounceRate: 0, avgDuration: 0 };
    const kpis = { ...rawKpis };
    const rawFunnel = period.funnel || { page_view: 0, view_item: 0, add_to_cart: 0, begin_checkout: 0, add_personal_info: 0, add_shipping_info: 0, add_payment_info: 0, purchase: 0 };
    const funnel = { ...rawFunnel };
    const sparklines = period.sparklines || { sessoes: [], users: [], purchases: [], rev: [], engagement: [], duration: [] };

    // Transform Devices to Array
    const dMobile = (period.devices && period.devices.mobile) || {};
    const dDesktop = (period.devices && period.devices.desktop) || {};
    const dTablet = (period.devices && period.devices.tablet) || {};
    
    const devices = [
      { name: 'Mobile', sessions: dMobile.sessions || 0, purchases: dMobile.ecommercePurchases || 0, revenue: dMobile.purchaseRevenue || 0, engRate: dMobile.engagementRate || 0, avgDuration: dMobile.averageSessionDuration || 0 },
      { name: 'Desktop', sessions: dDesktop.sessions || 0, purchases: dDesktop.ecommercePurchases || 0, revenue: dDesktop.purchaseRevenue || 0, engRate: dDesktop.engagementRate || 0, avgDuration: dDesktop.averageSessionDuration || 0 },
      { name: 'Tablet', sessions: dTablet.sessions || 0, purchases: dTablet.ecommercePurchases || 0, revenue: dTablet.purchaseRevenue || 0, engRate: dTablet.engagementRate || 0, avgDuration: dTablet.averageSessionDuration || 0 }
    ];

    // Transform Locations
    const transformLocation = (item, nameKey) => ({
      name: item[nameKey] || '(not set)',
      sessions: item.sessions || 0,
      purchases: item.ecommercePurchases || 0,
      revenue: item.purchaseRevenue || 0,
      engRate: item.engagementRate || 0,
      avgDuration: item.averageSessionDuration || 0
    });

    const regions = (period.regions || []).map(r => transformLocation(r, 'region')).filter(r => r.name !== '(not set)');
    const cities = (period.cities || []).map(c => transformLocation(c, 'city')).filter(c => c.name !== '(not set)');

    // Transform Products
    const products = [];
    const catsMap = {};
    
    (period.products || []).forEach(p => {
      if (!p.itemName || p.itemName === '(not set)') return;
      if (prodFilter && prodFilter !== 'all' && p.itemName !== prodFilter) return;
      if (catFilter && catFilter !== 'all' && p.itemCategory !== catFilter) return;

      const v = p.itemsViewed || 1;
      const prod = {
        name: p.itemName,
        category: p.itemCategory === '(not set)' ? 'unknown' : p.itemCategory,
        views: v,
        purchases: p.itemsPurchased || 0,
        revenue: p.itemRevenue || 0,
        engRate: 0.95,
        duration: 120
      };
      
      products.push(prod);
      
      if (!catsMap[prod.category]) {
        catsMap[prod.category] = { name: prod.category, views: 0, purchases: 0, revenue: 0, durSum: 0 };
      }
      catsMap[prod.category].views += prod.views;
      catsMap[prod.category].purchases += prod.purchases;
      catsMap[prod.category].revenue += prod.revenue;
      catsMap[prod.category].durSum += prod.views * prod.duration;
    });
    
    const categories = Object.values(catsMap).map(c => ({
      name: c.name,
      views: c.views,
      purchases: c.purchases,
      revenue: c.revenue,
      avgDuration: c.durSum / (c.views || 1)
    })).sort((a,b) => b.revenue - a.revenue);

    // Transform Channels/Sources
    const channels = [];
    const groupsMap = {};

    (period.sources || []).forEach(s => {
      let canal = "Nulo", grupo = "Nulo";
      if (s.sessionSourceMedium && s.sessionSourceMedium !== "(not set)") {
        const parts = s.sessionSourceMedium.split(" / ");
        const source = parts[0] ? parts[0].trim() : "";
        const medium = parts[1] ? parts[1].trim() : "";
        
        const utm = window.UTM_MAP[source];
        if (utm) {
          canal = utm.canal;
          grupo = utm.grupo;
        }
        const override = window.UTM_MEDIUM_OVERRIDES[medium];
        if (override) {
          if (override.canal) canal = override.canal;
          grupo = override.grupo;
        }
      }

      const ch = {
        name: s.sessionSourceMedium,
        group: grupo, // The UI expects 'group' property for aggregation
        canal: canal,
        grupo: grupo,
        sessions: s.sessions || 0,
        purchases: s.ecommercePurchases || 0,
        revenue: s.purchaseRevenue || 0,
        engRate: s.engagementRate || 0,
        bounceRate: s.bounceRate || 0,
        avgDuration: s.averageSessionDuration || 0,
        pageviews: Math.round((s.sessions || 0) * 1.5)
      };

      if (sourceFilter && sourceFilter !== 'all' && ch.name !== sourceFilter) return;
      if (canalFilter && canalFilter !== 'all' && ch.canal !== canalFilter) return;
      if (grupoFilter && grupoFilter !== 'all' && ch.grupo !== grupoFilter) return;

      channels.push(ch);

      if (!groupsMap[ch.group]) {
        groupsMap[ch.group] = { name: ch.group, sessions: 0, purchases: 0, engSum: 0, durSum: 0, pv: 0, revenue: 0 };
      }
      groupsMap[ch.group].sessions += ch.sessions;
      groupsMap[ch.group].purchases += ch.purchases;
      groupsMap[ch.group].engSum += ch.engRate * ch.sessions;
      groupsMap[ch.group].durSum += ch.avgDuration * ch.sessions;
      groupsMap[ch.group].pv += ch.pageviews;
      groupsMap[ch.group].revenue += ch.revenue;
    });

    const channelGroups = Object.values(groupsMap).map(g => ({
      name: g.name,
      sessions: g.sessions,
      purchases: g.purchases,
      engRate: g.engSum / (g.sessions || 1),
      avgDuration: g.durSum / (g.sessions || 1),
      pageviews: g.pv,
      revenue: g.revenue
    })).sort((a,b) => b.sessions - a.sessions);

    channels.sort((a,b) => b.sessions - a.sessions);
    
    // Update totals if a filter is applied
    const hasFilter = (sourceFilter && sourceFilter !== 'all') || (canalFilter && canalFilter !== 'all') || (grupoFilter && grupoFilter !== 'all') || (campaignFilter && campaignFilter !== 'all');
    if (hasFilter) {
      kpis.sessions = channels.reduce((sum, c) => sum + c.sessions, 0);
      kpis.purchases = channels.reduce((sum, c) => sum + c.purchases, 0);
      kpis.revenue = channels.reduce((sum, c) => sum + c.revenue, 0);
      
      const ratio = rawKpis.sessions ? (kpis.sessions / rawKpis.sessions) : 0;
      kpis.users = Math.round((rawKpis.users || 0) * ratio);
      kpis.newUsers = Math.round((rawKpis.newUsers || 0) * ratio);
      kpis.engagementRate = channels.reduce((sum, c) => sum + (c.engRate * c.sessions), 0) / (kpis.sessions || 1);
      kpis.bounceRate = 1 - kpis.engagementRate;
      kpis.avgDuration = channels.reduce((sum, c) => sum + (c.avgDuration * c.sessions), 0) / (kpis.sessions || 1);
      
      Object.keys(funnel).forEach(k => {
        funnel[k] = Math.round((rawFunnel[k] || 0) * ratio);
      });
    }

    return {
      kpis,
      channels,
      channelGroups,
      funnel,
      devices,
      regions,
      cities,
      products,
      categories,
      sparklines
    };
  };

  return {
    rawStart: startDate,
    rawEnd: endDate,
    currentText: `${startDate} até ${endDate}`,
    previousText: `${compareMode}`,
    current: processPeriod(window.GA4_RAW_DATA.current),
    previous: processPeriod(window.GA4_RAW_DATA.comparison || window.GA4_RAW_DATA.current)
  };
}

